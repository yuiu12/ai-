'''
摄氏度   1标准大气压下水的结冰点位0度，沸点位100度
华氏度   一标准大气压下水的结冰点位32度，沸点位212度
C = (F - 32) /1.8   C摄氏度    F华氏度
F = C * 1.8 + 32  
'''
num = input("请输入带有符号的温度值:") 

if num[-1] in ['C','c']:
    f = 1.8 * float(num[0:-1]) + 32 
    print(f"转换后的温度是{f:0.2f}华氏度") 
elif  num[-1] in ['F','f']:
    c = (float(num[0:-1]) - 32) / 1.8 
    print(f"转换hi的温度是{c:0.2f}摄氏度")
else:
    print("输入格式错误")
